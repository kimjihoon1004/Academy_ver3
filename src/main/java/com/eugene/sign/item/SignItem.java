package com.eugene.sign.item;

public class SignItem {
	private String id;
	private String tc_id;
	private String st_id;
	private String crs_id;
	private String monday;
	private String tuesday;
	private String wednesday;
	private String thursday;
	private String friday;
	private String sign_in;
	private String sign_out;
	
	public void setSignIn(String sign_in) {
		this.sign_in = sign_in;
	}
	public String getSignIn()	{
		return sign_in;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTc_id() {
		return tc_id;
	}
	public void setTc_id(String tc_id) {
		this.tc_id = tc_id;
	}
	public String getSt_id() {
		return st_id;
	}
	public void setSt_id(String st_id) {
		this.st_id = st_id;
	}
	public String getCrs_id() {
		return crs_id;
	}
	public void setCrs_id(String crs_id) {
		this.crs_id = crs_id;
	}
	public String getMonday() {
		return monday;
	}
	public void setMonday(String monday) {
		this.monday = monday;
	}
	public String getTuesday() {
		return tuesday;
	}
	public void setTuesday(String tuesday) {
		this.tuesday = tuesday;
	}
	public String getWednesday() {
		return wednesday;
	}
	public void setWednesday(String wednesday) {
		this.wednesday = wednesday;
	}
	public String getThursday() {
		return thursday;
	}
	public void setThursday(String thursday) {
		this.thursday = thursday;
	}
	public String getFriday() {
		return friday;
	}
	public void setFriday(String friday) {
		this.friday = friday;
	}
	public String getSignOut() {
		return sign_out;
	}
	public void setSignOut(String sign_out) {
		this.sign_out = sign_out;
	}
	
	
}
