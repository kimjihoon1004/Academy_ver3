package com.eugene.login.item;

public class LoginItem {
	private String loginMail;
	private String loginName;
	private String loginDate;
	private String loginGender;
	private String loginPhone;
	private String loginPw;
	private String loginMemo;	
	
	public String getLoginMail() {
		return loginMail;
	}
	public void setLoginMail(String loginMail) {
		this.loginMail = loginMail;
	}
	
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	
	public String getLoginPw() {
		return loginPw;
	}
	public void setLoginPw(String loginPw) {
		this.loginPw = loginPw;
	}
	
	public String getLoginDate() {
		return loginDate;
	}
	public void setLoginDate(String loginDate) {
		this.loginDate = loginDate;
	}
	
	public String getLoginGender() {
		return loginGender;
	}
	public void setLoginGender(String loginGender) {
		this.loginGender = loginGender;
	}
	
	public String getLoginPhone() {
		return loginPhone;
	}
	public void setLoginPhone(String loginPhone) {
		this.loginPhone = loginPhone;
	}
	public String getLoginMemo() {
		return loginMemo;
	}
	public void setLoginMemo(String loginMemo) {
		this.loginMemo = loginMemo;
	}
	
	
	
}
